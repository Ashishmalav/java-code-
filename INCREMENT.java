public class INCREMENT
{
    public static void main(String[]args)
    {
        int i=1;
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);
        int y=7;
        int c=--y+8;
        System.out.println(c);
    }
}
